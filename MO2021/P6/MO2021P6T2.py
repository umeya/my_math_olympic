from math import gcd

def MO2021P6T2():

    n, m, nm = 1, 2, 1*2
    while n <= 10**10:
        while not (gcd(n, m) ==1 and gcd(n+1, m+1) == 1):
            m += 2
        print(f'f({n})={m} , n={n} (= {nm}k - 1) ')
        nm = (n + 1) * (m + 1)
        n, m =  nm - 1, m + 2

if __name__ == '__main__':
    MO2021P6T2()

